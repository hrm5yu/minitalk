#include "libft.h"

int	ft_strcmp(char *a, char *b)
{
	int		i;

	i = 0;
	while (a[i] == b[i])
	{
		if (a[i] == '\0' && b[i] == '\0')
			return (1);
		i++;
	}
	return (1);
}
